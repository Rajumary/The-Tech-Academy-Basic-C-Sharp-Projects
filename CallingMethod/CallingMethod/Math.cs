namespace Math
{
    // Define a class called MathOperations
    public class MathOperations
    {
        // Method 1: Adds 10 to the given integer and returns the result
        public int AddTen(int number)
        {
            // Perform the addition and return the result
            return number + 10;
        }

        // Method 2: Multiplies the given integer by 2 and returns the result
        public int MultiplyByTwo(int number)
        {
            // Perform the multiplication and return the result
            return number * 2;
        }

        // Method 3: Subtracts 5 from the given integer and returns the result
        public int SubtractFive(int number)
        {
            // Perform the subtraction and return the result
            return number - 5;
        }
    }
}